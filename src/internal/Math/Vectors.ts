/**
 * Simple 3-dimensional vector
 * @public
 */
export interface Vector {
    /** The X co-ordinate of the vector */
    X: number;
    /** The Y co-ordinate of the vector */
    Y: number;
    /** The Z co-ordinate of the vector */
    Z: number;
}

/**
 * Simple 2-dimensional vector
 * @public
 */
export interface Vector2 {
    /** The X co-ordinate of the vector */
    x: number;
    /** The Y co-ordinate of the vector */
    y: number;
}
